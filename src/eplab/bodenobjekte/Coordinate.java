package eplab.bodenobjekte;

public class Coordinate {
//	protected long x, y, z;
	public long x, y, z;

	public Coordinate(long x, long y, long z) {
		this.x = x;
		this.y = y;
		this.z = z;
	}

	public long getX() {
		return this.x;
	}

	public long getY() {
		return this.y;
	}

	public long getZ() {
		return this.z;
	}

}
